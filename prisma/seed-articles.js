async function main() {
  console.log('بدء إضافة المقالات...');

  for (const article of ARTICLES) {
    try {
      const existing = await prisma.article.findUnique({ where: { slug: article.slug } });
      if (existing) {
        await prisma.article.update({ where: { slug: article.slug }, data: article });
        console.log(`✓ تم تحديث: ${article.title}`);
      } else {
        await prisma.article.create({ data: article });
        console.log(`✓ تم إضافة: ${article.title}`);
      }
    } catch (err) {
      console.error(`✗ خطأ في: ${article.title}`, err.message);
    }
  }

  console.log(`\nتم الانتهاء. ${ARTICLES.length} مقال في قاعدة البيانات.`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
